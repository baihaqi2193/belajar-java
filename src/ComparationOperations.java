public class ComparationOperations {
    public static void main(String[] args) {
        int a = 100;
        int b = 230;

        System.out.println("a = " + a + " b = " + b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
