public class NumberDataType {
    public static void main(String[] args) {
        // Integers
        byte iniByte = 100;
        short iniShort = 1_000;
        int iniInt = 1_000_000;
        long iniLong = 1_000_000_000;
        long ituLong = 100_000L;

        // Floating Points
        float iniFloat = 100.12F;
        double iniDouble = 200.20;

        // Literals
        int decimalInt = 25;
        int hexInt = 0xA132B;
        int binInt = 0b101011;
    }
}
