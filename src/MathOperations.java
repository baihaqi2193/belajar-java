public class MathOperations {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        // Math Operations
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);


        // Augmented Assignments
        a += 10; /*a = a + 10*/
        b -= 10;/*b = b - 10*/

        // Unary Operators
        a++; /*a = a + 1*/
        a--; /*a = a - 1*/
        --a; /*a = a - 1*/
    }
}
